import argparse, csv
from pathlib import Path
import yaml

import numpy as np
import trimesh
from scipy.spatial import cKDTree

def sample_surface(mesh, n, seed=0):
    np.random.seed(seed)
    return mesh.sample(n)

def nn_dist(a, b):
    tree = cKDTree(b)
    d, _ = tree.query(a, k=1, workers=-1)
    return d

def eval_sets(P, G, thrs_m):
    dp = nn_dist(P, G)   # pred -> gt
    dg = nn_dist(G, P)   # gt -> pred
    out = {}
    out["chamfer_l2_m2"] = float((dp**2).mean() + (dg**2).mean())
    out["acc_mean_m"]    = float(dp.mean())
    out["comp_mean_m"]   = float(dg.mean())
    for thr in thrs_m:
        prec = float((dp < thr).mean())
        rec  = float((dg < thr).mean())
        f = 0.0 if (prec + rec) == 0 else float(2 * prec * rec / (prec + rec))
        out[f"f@{thr*1000:.0f}mm"] = f
        out[f"prec@{thr*1000:.0f}mm"] = prec
        out[f"rec@{thr*1000:.0f}mm"] = rec
    return out

def load_as_points(path, n_samples):
    obj = trimesh.load(path, process=False)
    if isinstance(obj, trimesh.Trimesh):
        return sample_surface(obj, n_samples, seed=123)
    pts = np.asarray(obj.vertices)
    if pts.shape[0] > n_samples:
        idx = np.random.default_rng(123).choice(pts.shape[0], size=n_samples, replace=False)
        pts = pts[idx]
    return pts

def safe_get(d, keys, default=""):
    cur = d
    for k in keys:
        if not isinstance(cur, dict) or k not in cur:
            return default
        cur = cur[k]
    return cur

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--out_root", default="outputs/ablations_fixed")
    ap.add_argument("--gt", default="data/synth/scene0000/gt/gt_mesh.ply")
    ap.add_argument("--n", type=int, default=80000)
    ap.add_argument("--thr_mm", type=float, nargs="+", default=[10,20,50])
    ap.add_argument("--which", choices=["bootstrap","iso"], default="iso")
    args = ap.parse_args()

    out_root = Path(args.out_root)
    thrs_m = [t/1000.0 for t in args.thr_mm]

    gt_path = Path(args.gt)
    if not gt_path.exists():
        raise FileNotFoundError(f"GT not found: {gt_path}")

    G = load_as_points(str(gt_path), args.n)

    rows = []
    run_dirs = sorted(out_root.glob("run_*"))
    if not run_dirs:
        print(f"No run_* directories found under {out_root}")
        return

    for run_dir in run_dirs:
        run_name = run_dir.name
        run_id = run_name.split("_")[-1]  # "000"
        cfg_path = out_root / f"cfg_{run_id}.yaml"
        nbv_cfg_path = out_root / f"nbv_{run_id}.yaml"

        if not cfg_path.exists():
            # still allow eval if cfg is missing
            cfg = {}
        else:
            cfg = yaml.safe_load(cfg_path.read_text(encoding="utf-8")) or {}

        # which prediction to evaluate
        if args.which == "bootstrap":
            pred_path = run_dir / "meshes" / "tsdf_bootstrap.ply"
        else:
            pred_path = run_dir / "meshes" / "iso_points.ply"

        if not pred_path.exists():
            continue

        # read factors from cfg
        hetero = safe_get(cfg, ["model","heteroscedastic"], "")
        anchor = safe_get(cfg, ["model","use_anchor"], "")
        probes = safe_get(cfg, ["variance","probes"], "")

        # determine nbv enabled from the nbv yaml generated by ablations.py
        nbv_enabled = ""
        if nbv_cfg_path.exists():
            nbvc = yaml.safe_load(nbv_cfg_path.read_text(encoding="utf-8")) or {}
            nbv_enabled = safe_get(nbvc, ["nbv","enabled"], "")
        if nbv_enabled == "":
            nbv_enabled = (run_dir / "metrics" / "nbv.yaml").exists()

        nbv_util = ""
        nbv_out = run_dir / "metrics" / "nbv.yaml"
        if nbv_out.exists():
            d = yaml.safe_load(nbv_out.read_text(encoding="utf-8")) or {}
            nbv_util = safe_get(d, ["info","utility"], "")

        P = load_as_points(str(pred_path), args.n)
        met = eval_sets(P, G, thrs_m)

        rows.append({
            "run": run_name,
            "hetero": hetero,
            "anchor": anchor,
            "probes": probes,
            "nbv": bool(nbv_enabled),
            "nbv_utility": nbv_util,
            "pred": str(pred_path),
            **met
        })

    out_csv = out_root / f"summary_{args.which}.csv"
    if rows:
        keys = list(rows[0].keys())
        with out_csv.open("w", newline="", encoding="utf-8") as f:
            w = csv.DictWriter(f, fieldnames=keys)
            w.writeheader()
            for r in rows:
                w.writerow(r)
        print(f"Wrote: {out_csv} ({len(rows)} rows)")
    else:
        print("No runs evaluated. (Pred files missing?)")

if __name__ == "__main__":
    main()
